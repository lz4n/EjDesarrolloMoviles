package T1

fun main() {
    val operador1 = 2
    val operador2 = 3;

    println("$operador1 + $operador2 = ${operador1 + operador2}")
    println("$operador1 - $operador2 = ${operador1 - operador2}")
    println("$operador1 * $operador2 = ${operador1 * operador2}")
    println("$operador1 / $operador2 = ${operador1 / operador2}")
    println("$operador1 % $operador2 = ${operador1 % operador2}")
}
